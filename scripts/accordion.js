let toggleImg = document.getElementById('toggleImg')
let toggleFlag = false

function clickBtn(){
    if(toggleFlag){
        toggleImg.setAttribute('src','img/min.svg')
        toggleFlag = false
    }else{
        toggleImg.setAttribute('src','img/big.svg')
        toggleFlag = true
    }
}

